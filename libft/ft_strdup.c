#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	s1_l;
	size_t	i;

	s1_l = ft_strlen(s1) + 1;
	s2 = malloc(s1_l);
	if (s2 == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
